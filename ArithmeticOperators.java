
public class ArithmeticOperators
{
    public static void main(String[] args)
    {
        int a = 100, b = 25;
        System.out.print("Sum of a and b is: " + (a + b));
        System.out.print("\n");

        System.out.print("Sum of a and b is: " + (a - b));
        System.out.print("\n");

        System.out.print("Sum of a and b is: " + (a / b));
        System.out.print("\n");

        System.out.print("Sum of a and b is: " + (a % b));
    }
}
